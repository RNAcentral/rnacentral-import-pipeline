from pathlib import Path

import click

from rnacentral_pipeline.databases.expressionatlas import parser
from rnacentral_pipeline.writers import entry_writer


@click.group("expressionatlas")
def cli():
    """
    Commands for parsing expression atlas data
    """


@cli.command("parse")
# @click.option("--db-url", envvar="PGDATABASE")
@click.argument("ea_directory", type=click.Path(dir_okay=True, file_okay=False))
@click.argument("lookup", type=click.Path(dir_okay=False, file_okay=True))
@click.argument(
    "output",
    default=".",
    type=click.Path(writable=True, dir_okay=True, file_okay=False),
)
def process_csv(ea_directory, lookup, output):
    """
    Process the csv generated by linking EA data to rnc data
    """
    entries = parser.parse(ea_directory, lookup)
    print(entries.collect(streaming=True))
    exit()
    with entry_writer(Path(output)) as writer:
        try:
            writer.write(entries)
        except ValueError:
            print("No entries from this chunk")
